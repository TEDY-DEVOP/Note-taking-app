package com.example.notebook;

public class Note {
    String Title;
    String Content;


    public Note() {
    }

    public void setTitle(String title) {
        Title = title;
    }

    public void setContent(String content) {
        Content = content;
    }



    public String getTitle() { return Title;}

    public String getContent() {
        return Content;
    }

}
